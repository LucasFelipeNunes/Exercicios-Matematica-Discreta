"DESAFIO 1: Numa pesquisa sobre a preferencia em relacao a dois animes, foram consultadas 470 pessoas e o resultado foi o seguinte: 250 delas assistem Naruto Shippuden, 180 assistem Bleach e 60 assistem ambos."
total <- 470
naruto <- 250
bleach <- 180
ambos <- 60
"a) Quantas pessoas assistem apenas Naruto Shippuden?"
naruto - ambos
"b) Quantas pessoas assistem apenas Bleach?"
bleach - ambos
"c) Quantas pessoas assistem animes?"
naruto + bleach - ambos
"d) Quantas nao assistem animes?"
total - naruto - bleach + ambos